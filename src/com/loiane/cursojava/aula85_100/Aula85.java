package com.loiane.cursojava.aula85_100;

public class Aula85 {

	public static void main(String[] args) {

		System.out.println(Math.pow(2, 3)); //potencia
		
		System.out.println(Math.round(4.7));//arredonda .5 pra cima .4 pra baixo
		System.out.println(Math.round(4.4));
		
		System.out.println(Math.ceil(4.4)); //um numero para cima
		System.out.println(Math.ceil(4.7));
		
		System.out.println(Math.floor(4.4));//para baixo
		System.out.println(Math.floor(4.7));
		
		System.out.println(Math.round(Math.random() * 100));
		
		System.out.println(Math.sin(Math.toRadians(30))); //seno de 30 (toradians) em radiano
		System.out.println(Math.cos(Math.toRadians(1)));
		System.out.println(Math.tan(Math.toRadians(45)));
		//java não é totalmente segura para usar números
		
		//bognumber e bigdecimal - operações mais precisa
		
		
		
		
		
		
		
	}

}
